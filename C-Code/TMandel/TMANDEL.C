void plotline(int y, char buffer[]) {
 int x;

  for(x=0; x<xpoints; x++) {
 	if(buffer[x] == maxiterations) {
	 	plot(x,y,1);
	} else {
		plot(x,y,0);
	}
  }
}

int plot(int x, int y, int color)
{
/* Get screen base address */
unsigned char* screen = Physbase();

    /* Calculate screen memory offset */
    int offset = x/8 + y * 80;
                            
    /* Calculate bit mask for the pixel */
    int bit_mask = 0x80 >> (x %8);                             

    if (color == 1)
       *(screen + offset) |= bit_mask;
    else
       *(screen + offset) &= ~bit_mask;
                                                      
    return 0;
}

long drawpicture(void) {

  char line[320];
  int y;
  char double64[8];		/* a buffer for IEEE64 conversion */
  
  
  loadtr();             /* init Transputer and load binary */
  
  /* Sending the initial data to the Mandelbrot algorithm */
  puttrint(xpoints); 
  puttrint(ypoints);
  /* we have to convert the Pure C 80bit IEEE doubles 
     into the Transputers 64bit doubles.
	 'xdcnf' does that for us...     */
  xdcnv( &left, &double64 );  
  puttrdouble(double64); 
  xdcnv( &right, &double64 ); 
  puttrdouble(double64);
  xdcnv( &top, &double64 ); 
  puttrdouble(double64);
  xdcnv( &bottom, &double64 ); 
  puttrdouble(double64);
    
  puttrint(maxiterations);

  for (y=0; y<ypoints; y++) {
    gettrraw(line, xpoints);       /* Zeile von Transputer */
    plotline(y,line); 
  }
  
  return(0);
}


void newdrawpicture(int x1,int y1,int x2,int y2) {
  double temp;
  temp=left+(right-left)*(double)x1/(double)(xpoints-1);
  right=left+(right-left)*(double)x2/(double)(xpoints-1);
  left=temp;
  temp=top-(top-bottom)*(double)y1/(double)(ypoints-1);
  bottom=top-(top-bottom)*(double)y2/(double)(ypoints-1);
  top=temp;
  maxiterations*=2;
  drawpicture();
}

int loadtr(void) {   

	int check_result;
	unsigned char key;

  printf ("Checking for a connected Transputer...\n");

  check_result = checkTransputer(1);
  
  if (check_result == 4) {
	  printf ("Found a 32bit Transputer.Please choose binary:\n");
	  printf ("4 = T4xx (no FPU) / 8 = T80x (FPU) / 0 = Quit\n");
	  
	  init_t();

	  do {
		key = getchar();
	  } while(key!='4' && key!='8' && key!='0');
	  
	  if (key == '4') {
		printf ("Uploading T4xx binary...");
		puttrraw(mandel_t4, (sizeof(mandel_t8)));
	  } else if (key == '8' ) {
		printf ("Uploading T80x binary...");
		puttrraw(mandel_t8, (sizeof(mandel_t8)));
	  } else {
		printf ("Exiting. Bye!");
		exit(0);
	  }
  } else {
	  printf ("sorry, no Transputer found :-\(... press a key to exit");
	  (void) getch(); 
	  exit(0);
  }
  
  
  printf ("done. Press RETURN.\r\n");
  (void) getch(); 

  return(0); 
}

